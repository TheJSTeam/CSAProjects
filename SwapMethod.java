import java.util.*;

public class SpeedCode {

   public static void main(String[] args) {
      int[] a1 = {12, 34, 56};
      swap(a1,1,2);
      System.out.println(Arrays.toString(a1));
   }
   
   public static void swap(int[] toswap, int index1, int index2) {
      int base = toswap[index1];
      toswap[index1] = toswap[index2];
      toswap[index2] = base;
   }
   
}
